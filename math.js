let result = 0;

// return a number between 0 and 1
result = Math.random();
console.log(result);

result = Math.floor(0.9);
console.log(result); //0

result = Math.ceil(0.1);
console.log(result); //1

result = Math.PI;
console.log(result);

result = Math.pow(2,3);
console.log(result); //2^3

result = Math.sqrt(16);
console.log(result); //4